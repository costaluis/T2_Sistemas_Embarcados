  // iterate image height
  for (j=0; j<IMG_HEIGHT; j++)
  {
    // iterate image width
    for (i=0; i<IMG_WIDTH; i++)
    {
      // check the borders of image
      if (i == 0 || i == (IMG_WIDTH-1) || j == 0 || j == (IMG_WIDTH-1))
      {
        // set 0 if the pixel is a border pixel
        output[i][j] = 0;
      } else
      // operate the edge filter
      { 
        // set result to 1
        res = 1;

        // convolution proccess
        for (k=-1; k<2; k++)
        {
          for (l=-1; l<2; l++)
          {
            // does and bit operation to pixel with the 9 pixels around
            res = (res & img[i+k][j+l]);
          }
        }
        // write the result pixel into output array
        output[i][j] = (img[i][j] - res);
      }
    }
  }
  
