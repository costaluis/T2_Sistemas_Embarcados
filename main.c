  // print final clock cycle
  neorv32_uart0_printf("Ciclos: %x%x\n", cycles.uint32[1], cycles.uint32[0]);

  // print the output image
  for (i=0; i<IMG_HEIGHT; i++)
  {
    for (j=0; j<IMG_WIDTH; j++)
    {
      aux_print_hex_byte(output[i][j]);
    }
  neorv32_uart0_print("\n");
  }

